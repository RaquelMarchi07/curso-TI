var x = 2;
console.log("x:", x);
console.log("x += 2:", x += 2 ); // x = x + 2 -> 4
console.log("x -= 1:", x -= 1 ); // x = 4 então 4 - 1 -> 3
console.log("x *= 2:", x *= 2 ); // x = 3 então 3 * 2 -> 6 
console.log("x /= 3:", x /= 3 ); // x = 6 então 6 / 3 -> 2
console.log("x %= 2:", x %= 2 ); // x = 2 se o módulo é o resto da divisão e em 2/2 não sobra nada, então x é 0
